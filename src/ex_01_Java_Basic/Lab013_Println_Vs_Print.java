package ex_01_Java_Basic;

public class Lab013_Println_Vs_Print {
    public static void main(String[] args) {
        System.out.println("Hello World!"); // print with new line
        System.out.println("How are you doing?"); // print with new line

        System.out.print("Hello World.");
        System.out.print("How are you?"); //print in same line as "Hello World."
    }
}
