package ex_02_Java_Basics_part2;

public class Lab015_Variables {
    public static void main(String[] args){
        /*
        data_type variable_name = variable_value
         */
        byte b = 10;
        b = 19;
        // byte is data type -> type of container
        // stores only integer value limited to -128 and 127 (max and min)
        // b -> variable name (identifier)
        // = -> assignment operator (used to assign value to b)
        // 10 -> Literal (integer value)
        System.out.print(b);
    }
}
