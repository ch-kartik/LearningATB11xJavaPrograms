package ex_07_Increment_Decrement_Operators;

public class Lab072_Increment_Decrement {
    public static void main(String[] args) {
        int a = 10;
        int result = ++a;
        System.out.println(a);
        System.out.println(result);

        // Line No | a | Exp (result)
        // 5 | 10 | NA
        // 6 | 11 | 11
    }
}
