package ex_04_Operators;

public class Lab037_Arithmetic_Operators {
    public static void main(String[] args) {
        //Arithmetic Operators
        //Addition -> +
        //Substraction -> -
        //Multiplication -> *
        //Division -> /
        //Modulus -> %  (Remainder)

        int a = 20;
        int b = 3;
        float c = 3.14f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/c); //6.3694267
        System.out.println(a%b);

        System.out.println("a+b");

    }
}
